#include "3-calc.h"
/**
 * op_add - adds two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result of substraction of the two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the product of the two numbers
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the result of division of the two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - Finds the remainder of division of two numbers
 * @a: the first number
 * @b: the second number
 *
 * Return: the remainder of the division of the two numbers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
